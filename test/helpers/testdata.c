/* Defines these functions:
 *   td_get0_param - get the param as a static BIGNUM
 *   td_get1_param - get a copy of the param as a BIGNUM
 * 
 * And also a const size_t for the size of the array:
 *   td_param_size - size of array in bytes
 */
#define BN_GETTER_IMPL(param)                  \
static BIGNUM *bn_##param = NULL;              \
BIGNUM *td_get0_##param##_bn() {               \
    if (bn_##param == NULL) {                  \
        bn_##param = BN_bin2bn(td_##param, sizeof(td_##param), NULL); \
        sk_BIGNUM_push(td_bns, bn_##param);    \
    }                                          \
    return bn_##param;                         \
}                                              \
BIGNUM *td_get1_##param##_bn() {               \
    return BN_bin2bn(td_##param, sizeof(td_##param), NULL); \
}                                              \
const size_t td_##param##_size = sizeof(td_##param)

